import { useState } from "react";
import { Data } from "./components/Data";
import * as XLSX from "xlsx";

function App() {
  // on change states
  const [excelFile, setExcelFile] = useState(null);
  const [excelFileError, setExcelFileError] = useState(null);

  // submit
  const [excelData, setExcelData] = useState(null);
  // it will contain array of objects

  // handle File
  const fileType = [
    "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
  ];
  const handleFile = (e) => {
    let selectedFile = e.target.files[0];
    if (selectedFile) {
      // console.log(selectedFile.type);
      if (selectedFile && fileType.includes(selectedFile.type)) {
        let reader = new FileReader();
        reader.readAsArrayBuffer(selectedFile);
        reader.onload = (e) => {
          setExcelFileError(null);
          setExcelFile(e.target.result);
        };
      } else {
        setExcelFileError("Please select only excel file types");
        setExcelFile(null);
      }
    } else {
      console.log("plz select your file");
    }
  };

  // submit function
  const handleSubmit = (e) => {
    e.preventDefault();
    if (excelFile !== null) {
      const workbook = XLSX.read(excelFile, { type: "buffer" });
      const sheetNames = workbook.SheetNames;
      const sheetsData = {};

      sheetNames.forEach((sheetName) => {
        const sheet = workbook.Sheets[sheetName];
        const sheetData = XLSX.utils.sheet_to_json(sheet);

        sheetsData[sheetName] = sheetData;
      });
      // const worksheetName = workbook.SheetNames[0];
      // const worksheet = workbook.Sheets[worksheetName];
      // const data = XLSX.utils.sheet_to_json(worksheet);
      setExcelData(sheetsData);
    } else {
      setExcelData(null);
    }
  };

  return (
    <div className="container px-10 pt-6">
      {/* upload file section */}
      <div className="form">
        <form className="form-group" autoComplete="off" onSubmit={handleSubmit}>
          <label>
            <h5 className="font-medium">Upload Excel file</h5>
          </label>
          <input
            type="file"
            className="border border-2 max-w-full"
            onChange={handleFile}
            required
          ></input>
          {excelFileError && (
            <div className="text-danger" style={{ marginTop: 5 + "px" }}>
              {excelFileError}
            </div>
          )}
          <br></br>
          <button
            type="submit"
            className="bg-green-600 text-white rounded-sm px-2 py-1"
            style={{ marginTop: 5 + "px" }}
          >
            Submit
          </button>
        </form>
      </div>

      <br></br>
      <hr></hr>
      <br></br>

      {/* view file section */}
      <h5 className="font-medium mb-2">View Excel file</h5>
      <div>
        {excelData === null && (
          <div className="flex bg-gray-200 h-28 items-center justify-center">
            No file selected
          </div>
        )}
        {excelData !== null && (
          <div>
            <table className="table-fixed border-1">
              <tbody>
                <Data excelData={excelData} />
              </tbody>
            </table>
          </div>
        )}
      </div>
    </div>
  );
}

export default App;
